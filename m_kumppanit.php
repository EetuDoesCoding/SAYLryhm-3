<p>Moni muistaa SAYL:n tarjoamat edulliset Amerikan-lennot, joihin liiton jäsenet olivat oikeutettuja 1960-1980 luvulla. Toiminta hiipui vähitellen ja päättyi kokonaan Suomen uudistuneen Valmismatkalain myötä vuonna 1995. Matkailijoiden turvaksi laadittu Valmismatkalaki edellyttää jokaista matkanjärjestäjää tallettamaan liikevaihtoonsa suhteutetun rahasumman Kilpailu- ja Kuluttajavirastoon vakuudeksi matkanjärjestäjän mahdollisen konkurssin tai muun taloudellisen ongelmatilanteen varalta. SAYL on ystävyysseura ja meillä ei tällaiseen ollut eikä ole vieläkään mahdollisuuksia.</p>
<p>Teemme nykyään yhteistyötä useiden matkatoimistojen kanssa. Jäsenille tarjottavien USA-teemamatkojen vastuullinen matkanjärjestäjä vaihtelee. Pyydämme tarjouksia useasta yrityksestä ja päätämme yhteistyökumppanit hinnan, ohjelman ja muiden matkaehtojen perusteella tapauskohtaisesti.</p>
<p>Käyttämiämme matkatoimistoja, jotka myöntävät myös alennusta SAYL:n jäsenkunnalle Yhdysvaltoihin suuntautuvista pakettimatkoista, ovat</p>
