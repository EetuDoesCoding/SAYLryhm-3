<h2>USA:N MATKAILU</h2>
</article>
<ul style="list-style-type: none;">
  <li>
<p>Yhdysvallat on kooltaan maailman suurimpia valtioita, pinta-ala on lähes 10 000 000 neliökilometriä. Maa on kartasta katsottuna kuin tilkkutäkki, jossa 50 erikokoista ja hyvin erilaista palaa, osavaltiota, on liitetty yhteen.</p>
<p>Maan asukkaat, yli 300 miljoonaa ihmistä, puhuvat pääasiassa englantia, mutta varsinkin espanja äidinkielenä lisääntyy vauhdilla. Amerikkalaisista 80 prosenttia asuu kaupungeissa, 20 prosenttia maaseudulla. Suurimmat taajamat ympäristöineen ovat New York, Los Angeles ja Chicago.</p>
<p>Väestöstä valtaosa (82 %) on valkoihoisia, mustia on 13 %, aasialaisia 4 % ja Amerikan intiaaneja, inuiitteja sekä aleutteja vajaa prosentti. Uskonto on monelle amerikkalaisille oleellinen osa elämää; 56 % on protestantteja, 27 % katolisia, 2 % juutalaisia ja 1,5 % muslimeja.</p>
<p>Yhdysvallat voidaan jakaa selkeästi seitsemään osaan (Koillisosa, Itärannikko, Keskilänsi, Vuoristoalue, Lounaisosa, Syvä Etelä, Länsirannikko). Tämä ei ole virallinen jako, mutta noudattelee maan asukkaiden, kulttuurin ja maantieteen kokonaisuuksia.</p>
<p>Eri osien historia sekä jopa puhuttu kieli tai murre ja paikalliset tavat vaihtelevat runsaastikin. Tämä kaikki kuvastaa maan monimuotoisuutta ja mielenkiintoisuutta. Yhdysvalloista löytyy lähes kaikkea kaikille.</p>
