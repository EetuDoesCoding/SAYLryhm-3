<h2>SUOMI-USA</h2>
</article>
<ul style="list-style-type: none;">
  <li>
<p>Vuodesta 1946 ilmestynyt Suomi-USA -lehti on SAYL:n jäsenlehti. Lehti ilmestyy 4 kertaa vuodessa ja se kertoo liiton sekä jäsenyhdistysten toiminnasta ja tapahtumista.</p>

<p>Lehti kertoo lisäksi monipuolisesti:<br>
- USA:n matkakohteista ja matkoista<br>
- Mielenkiintoisista ihmisistä<br>
- Omakohtaisista kokemuksista USA:ssa ja amerikkalaisten kokemuksista Suomessa<br>
- Kulttuurin osa-alueista, ilmiöistä ja tapahtumista<br>
- Liiton apurahasäätiön stipendeistä ja stipendiaateista<br>
- Jäseneduista</p>

<p>Suomi-USA – lehteä on julkaistu säännöllisesti vuodesta 1946 lähtien. Lehden päätoimittaja on toiminnanjohtaja Lena Hartikainen.</p>

<p>Jäsenille Suomi-USA-lehti kuuluu 34 euron jäsenmaksuun. Lehden voi tilata myös kestotilauksena (neljä numeroa vuodessa) kotimaahan 45 €, muut maat 55 €.</p>

