  <h5>Yhdysvaltojen poliittinen järjestelmä (2012)</h5>
  <p>Mitkä on Yhdysvaltain perustuslain aatteelliset periaatteet? Mikä on pocket veto? Entä miten toimii maan oikeusjärjestelmä?</p>
  <p>Yhdysvaltojen poliittiset päätökset vaikuttavat laajasti koko maailmaan. Maan poliittinen järjestelmä on kuitenkin niin monisäikeinen ja erilainen kuin täällä koti-Suomessa.</p>
  <p>Yhdysvaltojen poliittinen järjestelmä-esitteen paperiversion hinta on 5 €.</p>
  <p>Voit tutustua ja lukea pdf-muodossa olevaa esitettä myös sähköisesti
  <a href="http://sayl.fi/wp-content/uploads/2013/04/Ypj2012_suojattu.pdf">tästä.</a></p>

  <h5>Yhdysvallat opas (2011)</h5>
  <p>Olemme julkaisseet yhteistyössä Suomi-Seuran kanssa päivitetyn painoksen Yhdysvaltoihin muuttajan oppaasta. Siitä selviää kätevästi moni asia: muutto Suomesta, verotus, viisumit, sosiaaliturva, Suomi-kontaktit... Oppaan lähes 60 sivulla on valtavasti faktaa Suomesta Atlantin taakse töihin tai opiskelemaan lähtevälle.</p>
  <p>Oppaan jäsenhinta on  12 € + postikulut, muut 20 € + postikulut.</p>

  <h5>Suomenkielinen Welcome to America -matkaopas (2010) PAINOS LOPPU </h5>
  <p>Tammikuussa 2006 ensimmäisen kerran julkaistu monipuolinen Welcome to America -matkaopas on nyt päivitetty.  84-sivuisesta kirjasesta löydät kätevästi yleistietoa Amerikasta sekä yksityiskohtaisempia faktoja ja vinkkejä jokaisesta maan 50 osavaltiosta. Autoilevalle matkailijalle on koottu amerikkalaisiin liikennesääntöihin ja -tapoihin liittyvät sivut. Kirjasen julkaisija on Suomi-Amerikka Yhdistysten Liitto.</p>
  <p>Matkaoppaan hinta on 5 €.</p>

  <h5>Kalevala englanniksi (2006, uusittu painos)</h5>
  <p>SAYL:n kustantama 80-sivuinen englanninkielinen, etenkin ulkomaalaisille tarkoitettu kirjanen Kalevalasta on suosittu lahja ja muisto Suomesta.</p>
  <p>Kirjan kirjoittajat Heikki Kirkinen ja Hannes Sihvo ovat “The Kalevala, an epic of Finland and all mankind” -kirjassaan pyrkineet nimenomaan suomalaisen kulttuurin ja kirjallisuuden esittelyyn Kalevalan kauniiden runojen avulla. Opus kertoo myös kansalliseepoksemme synnystä ja sen kirjoittajasta sammattilaisesta Elias Lönnrothista (1802–1884).</p>
  <p>Kauniit kuvat lukuisista Kalevala-aiheisista tauluista täydentävät kirjasen, mukana on mm. Akseli Gallen-Kallelan Lemminkäisen äiti ja Pekka Halosen sekä Robert Wilhelm Ekmanin maalaukset Väinämöisestä soittamassa kanteletta.</p>
  <p>Kalevan hinta on 8 € + postituskulut.</p>

  <h5>Yhdysvaltain historiahaitari (PAINOS LOPPU)</h5>
  <p>SAYL:n kustantama USA:n historiaa löytöretkistä 2000-luvulle -historiahaitari kertoo maan ja kansakunnan tärkeimpiä kehitysvaiheita, henkilöitä ja tapahtumia.</p>
  <p>Historiahaitarin hinta on 5 €.</p>
