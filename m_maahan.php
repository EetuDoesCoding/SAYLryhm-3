<h2>ESTA-MAAHANTULOLOMAKE</h2>
</article>
<ul style="list-style-type: none;">
  <li>
<p>Yhdysvallat otti käyttöön tammikuussa 2009 sähköisen ESTA- maahantulolomakkeen (Electronic System for Travel Authorization, joka korvaa lentokoneessa täytetyn paperisen maahantulolomakkeen.</p>
<p>Kaikkien viisumivapausmaiden kansalaiset (joihin myös Suomi kuuluu), jotka matkustavat Yhdysvaltoihin lyhyelle liike- tai turistimatkalle (=alle 90vrk), joutuvat anomaan ESTA-luvan ennen kuin voivat nousta Yhdysvaltoihin menevälle lennolle tai laivaan. Myös lapset iästä riippumatta, ja huolimatta siitä matkustavatko yksin tai huoltajansa kanssa, tarvitsevat oman ESTA-luvan.</p>
<p>Lomake koskee myös Yhdysvaltojen kautta matkustavia matkustajia, joilla on pelkkä koneenvaihto Yhdysvalloissa. Lomakkeesta liikkuu erilaisia huijausversioita Internetissä.</p>
<p>Henkilöt, joilla on voimassaoleva viisumi Yhdysvaltoihin, käyttävät viisumia, jos matkan tarkoitus ei ole muuttunut viisumin myöntämisen jälkeen. Viisumilla matkustavat henkilöt eivät tarvitse ESTA-matkustuslupaa.</p>
<h5>Kuinka matkustuslupaa anotaan?</h5>
<p>ESTA-luvan voi anoa ainoastaan nettiosoitteesta
<a href="https://esta.cbp.dhs.gov/esta/">https://esta.cbp.dhs.gov</a>
Vastaa suomenkielisiin kysymyksiin annettujen ohjeiden mukaisesti ja lähetä sähköinen hakemuksesi käsiteltäväksi. Luvan hakeminen muuttui maksulliseksi 8.9.2010, 14 dollarin maksun voi maksaa luottokortilla.</p>
<h5>Kauanko ennen matkaa ESTA-matkustuslupaa on anottava?</h5>
<p>DHS (Department of Homeland Security) suosittelee, että ESTA-matkustuslupaa anotaan viimeistään 72 tuntia ennen matkan alkamista. Anomuksen voi kuitenkin jättää milloin tahansa ennen Yhdysvaltoihin suunniteltua viisumivapaata matkaa.</p>
<p>Suurimman osan kohdalla ESTA-järjestelmä antaa lähes välittömän vastauksen onko matkustaja oikeutettu käyttämään viisumivapausohjelmaa matkustaessaan Yhdysvaltoihin. Vaihtoehtoisia vastauksia on kolme: Anomus Hyväksytty, Anomus Kesken tai Anomus Hylätty. Matkustajat, joiden anomus hylätään, ohjataan anomaan viisumia ohjeiden mukaisesti.</p>
<p>Lisätietoja ESTA-matkustusluvasta ja viisumiasioista löytyy Yhdysvaltain suurlähetystön nettisivuilta:
  <a href="https://finland.usembassy.gov/visas.html">http://finland.usembassy.gov/visas.html</a></p>
