<h2>TARVITSENKO VIISUMIA?</h2>
</article>
<ul style="list-style-type: none;">
  <li>
<p>Useimmat suomalaiset eivät tarvitse viisumia Yhdysvaltoihin. Suomen kansalaisia, jotka täyttävät seuraavat Visa Waiver Pilot Program -ehdot (=viisumivapausohjelma), kehotetaan matkustamaan ilman viisumia. 12.1.2009 lähtien suomalaisten ja kaikkien viisumivapausohjelmalla Yhdysvaltoihin matkustavien maiden kansalaisten on anottava ennen matkaansa sähköinen ESTA-matkustuslupa.</p>

<h5>Suomen kansalaiselta edellytetään, että</h5>
<p>- hänellä on voimassaoleva koneellisesti luettava Suomen passi (=Suomen kansalaisuus). Lapset tarvitsevat oman koneellisesti luettavan passin.<br>
- Suomen passin on tulee olla voimassa kyseisen matkan ajan<br>
- matkan tarkoitus on turismi tai business<br>
- oleskelu Yhdysvalloissa kestää alle 90 vuorokautta<br>
- hänellä on meno-paluulippu<br>
- hänellä on riittävästi varallisuutta kattamaan koko oleskeluaika<br>
- hän täyttää I-94W -maahantulolomakkeen (saa lentoyhtiöstä)<br>
- hän saapuu Yhdysvaltoihin joko lento-tai meriteitse sellaisen yhtiön aluksella, joka osallistuu viisumivapausohjelmaan (kaikki tärkeimmät lentoyhtiöt ovat mukana sopimuksessa), tai maitse, jolloin meno-paluulippua ei tarvita, vaan hän muutoin pystyy osoittamaan väliaikaisen oleskelun</p>

<h5>Viisumivapautta ei voi käyttää henkilö</h5>
<p>- jolla on rikosrekisteri<br>
- jolta on aikaisemmin evätty viisumi, joka on käännytetty Yhdysvaltain rajalta tai joka on karkoitettu maasta<br>
- joka on huumausaineiden käyttäjä tai kuljettaja<br>
- joka psyykkisen tai fyysisen häiriön vuoksi on ollut tai tulee olemaan vaaraksi itselleen tai muille<br>
- jolla on ollut yhteys natsivainoihin<br>
- joka on kieltäytynyt luovuttamasta lapsen huoltajuutta Yhdysvaltain kansalaiselle, jolle huoltajuus on määrätty<br>
- joka on vaatinut diplomaattista koskemattomuutta<br>
- joka sairastaa tarttuvaa tautia (esim. tarttuva tuberkuloosi)</p>
<p>Mikäli jokin edellämainituista seikoista koskee teitä, ottakaa yhteys Yhdysvaltain suurlähetystön konsulaattiin spostitse, HelsinkiNIV@state.gov</p>
<p>Viisumivapaus ei myöskään koske henkilöä, jonka matkan tarkoitus on opiskelu, työ (myös au-pair, mediatyö) tai matkan kesto on yli 90 vuorokautta. Lisätietoja viisumeihin liittyvistä asioista löytyy Yhdysvaltain suurlähetystön nettisivuilta,
<a href="https://finland.usembassy.gov/visas.html">http://finland.usembassy.gov/visas.html</a></p>
